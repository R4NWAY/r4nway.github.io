            <h5>What do you do when you witness a child crying?</h5>
            <p>
              What do you do when you witness a child crying? They are not yours. They are not abused. Just ignored by their parents. Do you react? Or do you mind your own business?<br />
              Most of us choose the latter, which means it is socially correct or acceptable. But is that best for the child? For parents? For us? For all other observers and bystanders?<br />
              Is that right beyond question? Is that truly respectful? Is that the noblest behavior?<br />
              It is so common we accepted it as a social norm and stopped questioning. But if it is best what we can do, why does it leave us with a cramp in a stomach so often?<br />
            </p>

            <h5> Losing faith</h5>

            <p> “Mama! Mama!” a little boy was screaming desperately as he observed his parents walking away. He tried to keep up with them, but it was not easy for a three-year-old, tucked in a proper winter attire restricting his mobility on a
              slippery slope covered with snow. He kept falling and eventually started to throw himself on the snow with despair and screamed even more hysterically, hoping that mama would come back, pick him up and be at his reach again.<br />
              She did not. Neither did the father.

            </p>
            <p> So, the little one was lamenting louder and louder, attracting the attention of all adults and kids on the sledding slope. He suffered. It was clear for all people around. Not for his parents, though. They kept walking up,
              sporadically looking back and laughing, in their mind probably teaching the little human that being 50 or 100 meters behind parents in this unusual place and climbing the snowy slope was not a big deal.

            </p>
            <p> It was a big deal. From his three-year-old perspective, not only did he genuinely struggle to climb up, but as he expressed his struggle, he was ignored, abandoned, and laughed at.<br />
              Not just once. Each time one of his parents slid down with him for maybe 15-20 seconds. After this moment of fun, we witnessed again an adult walking up the hill at an adult pace, and the toddler left behind, crawling, falling, and
              crying for 10-15 minutes before he finally reached the top and deserved another moment of connection. Within 2-3 acts of this spectacle, all other families left the slope. Babies were picking up the mood and becoming cranky. Adults
              were not comfortable either.

            </p>
            <p> My three-year-old daughter witnessing the drama of her age-mate turned to me and said: “Mom, the little boy is crying because he is sad.”<br />
              SAD.<br />
              Not because he had a temper tantrum, tried to manipulate, or power play.<br />
              He cried because he was sad.<br />
              It was so clear for a three-year-old.<br />
              Unfortunately, it wasn’t for his parents.<br />
              Cold, unresponsive, ignorant, or maybe only tired? Doesn’t really matter. They did not want to see that their child is crying after them. In a three-year-old mind and heart, he was left alone; he was left behind. He saw the backs of
              people he knew, loved, and trusted moving away and not looking back. He cried “mama,” and mama hadn’t reacted. <br />
              There’s nothing worse for a child than being unseen, unheard, unnoticed. Invisible. Unimportant.
            </p>
            <h5> What would you do? </h5>

            </p>
            <p> Would you save the child and taught parents a lesson? (Oh yes, in my fantasy, I sometimes wish to be a Zorro that helps the oppressed ones and pays back the oppressors)<br />
              Would you pick up the child and help him climb?<br />
              Would you try to talk with the parents convincing them their parenthood model might be based on outdated beliefs?

            </p>
            <p> What have I done?<br />
              None of the above. Regrettably.<br />
              That’s why it bothered me so much and made me contemplate my own reaction for a good couple of days.

            </p>
            <p> I was standing there looking at a poor child and his oblivious parents, holding my little one by her hand and hesitating whether I should do something at all or just let them raise their child their way…

            </p>
            <p> Would any reaction be accepted or appreciated, especially when “social distancing” is the expected norm? (what a lousy excuse now for not reacting!)<br />
              Would they understand me?<br />
              Would their ego allow them even to consider that I’m trying to help them?<br />
              Or would they take it even more on the poor little boy to validate their way (it’s your fault, you’re a loser, shame on you, etc.)?

            </p>
            <p> I allowed these doubts to silence my motherly intuition, my human instinct.

            </p>
            <p> I would love to be the hero in this story, and I wasn’t.<br />
              Nobody was.

              <h5> Therapist’s perspective. </h5>

            </p>
            <p> Some people would argue that no harm was done to the child. He was not abandoned; he was not abused; he was not even yelled at. It is not that bad; there are always worse parents… Some may even argue that such “exercise” would
              toughen a child and teach them to be more independent.<br />
              Nothing further from the truth.

            </p>
            <p> I can’t tell you how many times my clients move back to events in their life when they felt left behind, unnoticed, unimportant, or “just” less important than another child, activity, thing… It is one of the most common denominators
              of many struggles in their lives later. Often underpinned with a belief <br />
              “I am not worthy. Not worthy of attention, care, love. I have no voice. I don’t matter.”.<br />
              This conviction gets deep down into their hearts and minds so early that it becomes natural, and they stop noticing its presence. But it is there, and it limits and hurts their souls for many years.

            </p>
            <p> It is also what drives them to study and work painstakingly, prove themselves, achieve, deliver, and accomplish. To earn attention, to get recognition, to feel noticed and important. Yet, these feelings don’t last. Moments of
              triumphs seem short, satisfaction vanishes quickly, another trophy, award, promotion, title, or honour do not make them feel lovable. The feeling of “unworthiness” prevails in their lives.

            </p>
            <p> It is not only something experienced in pathological families, where kids are truly abandoned, neglected, abused, and rejected. It happens more often than we want to admit and notice. It can happen when a child is sent away to
              boarding school way before he/she is ready to be separated from the family and suffers for days, weeks, months and years. Enough time to ingrain “I am not good enough, I am not worthy” belief in a young mind.<br />
              It can happen when tired parents regularly have no time or energy to play or spend time with their little ones.<br />
              It can happen when something important happens in the family (loss of a job, a birth, an illness, divorce, accident, etc.) and no one talks with the child to explain it to them. When they feel lost, scared, confused, and then realized
              they are unheard, unseen, unimportant.

            </p>
            <p> It is often not the event itself that makes them feel and think this way. It is the adult’s reaction to whatever happens.<br />
              Aggressive reaction. "It’s none of your business! It is your fault!"<br />
              Diminishing reaction. "You’re exaggerating. Don’t be such a baby."<br />
              Annoyed reaction. "What are you doing here? Why do you always have to bother me? Go to your room!<br />
              Dismissive reaction. "Nothing really happened. You imagine stuff, as always."<br />
              Or … lack of reaction at all. As if the child was not there. As if they haven’t seen, haven’t heard, haven’t experienced. As whatever happened had not impacted them at all. As the fact that they are scared, confused, or lost, is not
              even noticed or doesn’t matter to anybody.

            </p>
            <p> Don’t get me wrong. There are no ideal parents, and I am far from declaring it. And I don’t intend to focus on these parents. I want to focus on me, on us, on witnesses of such an event.

              <h5> Ethical question. </h5>

            </p>
            <p> Let’s try to evaluate this problem from a moral angle.<br />
              Let’s leave out the emotional “be a Zorro and teach these parents a lesson” approach.<br />
              Let’s go beyond the option of what we could call “a well-intended but selfish attempt”, meaning <br />
              “I tried to do something. Regardless of whether it worked or didn’t, I have a clear conscience as I tried to help.”<br />
              Let’s focus on the fragile human being who was in the very center of this relatively moderate scene (no obvious harm, no physical violence), and ask ourselves a question: Is it better for the small child to get attention and reaction
              from others, even if they are not the ones, he seeks attention from? Or is it better not to intervene with the family dynamics, given no legally punishable act was taking place?

            </p>
            <p> A small child with a still immature nervous system and limited ability to verbally express his emotions does not cope with the situation. With a lack of response from the parents, he falls into what some call “a temper tantrum”, a
              hysterical reaction to the situation that overwhelmed him.<br />
              He communicates in the only way available to him at that moment that he is unhappy, maybe tired, certainly vulnerable. He screams for attention, security & contact that he needs to calm down. He needs to know that he is noticed, heard,
              not abandoned.
            </p>

            <p> Let’s assume that a well-hearted stranger recognizes the little boy, comes closer, catch eye contact, talks to him, and manages to do that without scaring the already scared toddler. A stranger that breaks the “I am invisible, unimportant, and
              no matter how loud I cry, there is no reaction” idea that already formulates in the very young mind.<br />
              A stranger that gives hope that in this unbearable situation when “my parents let me down, there is somebody out there that will see me, hear me, help me”.<br />
              Is this an act of kindness, goodness, generosity? The light of hope that now and then will lit up in his heart and mind, and perhaps give him enough faith that will help him keep the head above the water in the future moments of
              despair?
              Like the bellowed grandma who gives a child all the love that parents missed to express, or like the teacher that spotted potential in a child that nobody else believed in.<br />
              <br />
              Or is it actually making the situation much worse as it gives the child a string of hope that will not materialize frequently? Will that cause more emotional volatility by leading him to believe that help might be out there, by making
              him wait and look for it, and disappointing him most of the time? Isn’t it better instead to allow him to face reality, get used to it, and learn to cope?<br />
<br />
              Which way can cause more damage? Which has greater potential to help him grow and develop an ability to cope, a pinch of confidence, a drop of trust in the human heart?

              <h5> My choice. </h5>

            </p>
            <p> I base my perspective on my therapeutical experience and years of passionate studies of the subject of confidence, mental health,
              and psychosomatic diseases. What we often call “resilience” is indeed <br />
              a coping mechanism, but sometimes
              not the one that strengthens us and builds us up. As an adult we have a choice and a power to decide “What doesn’t kill me makes me stronger, I will not give up!” and reposition an adverse situation into “a learning experience”. We
              don’t have such an ability at the age of three.<br />
              Children’s survival and wellbeing depend on their caregivers responding to their needs. When they are hungry, thirsty, in pain, cold, lonely, overwhelmed, they rely on an adult to be comforted. A cry is the very first way of
              expression, gradually replaced by more sophisticated means of communication.<br />
              A child who experiences that “cry doesn’t help, I don’t get attention, warmth, love, contact I need; it takes energy out, but doesn’t work”, or “when I cry my parents get angry, laugh, humiliate me, and like me even less”, learns quickly
              to suppress emotional pain. They quickly learn not to cry, and to be “a big boy or girl”. Ironically, many unaware adults reinforce and applaud such change as a sign of great maturity and strength (?!!!). But a three-year-old who
              does not cry when he feels abandoned, tired, or hurt is not strong. He is scared. Scared that his cry will make things worse.

            </p>
            <p> So what will I do in similar circumstances next time?<br />
              My choice is to be a courageous and warm-hearted stranger that will come to a child, speaks to them gently, asking: “Hello, are you ok?”.<br />
              If they don’t respond. I would stay about 2 meters away and still try to connect, asking:<br />
              – “Is that your mama up there?”<br />
              – “Can I wait here until she comes down?”<br />
              – “Do you want me to call your mama?”<br />
              There is a chance that they would nod to one of these as getting close to mama is what they want.

            </p>
            <p> If they connect and respond, we have a chance to move on. Whichever way they respond, yes or no, is not important. What matters is that they hear and connect, which takes them out of the stressful emotion and tiring cry. We can start
              the conversation:<br />
              – “I can see you are upset, right?” (Naming the emotion they experience helps children handle the stress. Not only do they feel noticed and understood but it also makes the scary infinite feeling a bit more defined.)<br />
              – “Is that your mama up there waiting for you? It is not easy to climb, is it? Can I walk next to you so we can slowly get to your mom?”<br />

            </p>
            <p> I would try not to invade the physical space of the child, definitely not to hug (expressing our motherly instincts toward not our children is crossing the line from my point of view), but even not to take by the hand unless they
              extend them, and we confirm they want help in climbing.<br />
              My choice is to be a stranger who helps the toddler to find their strength and encourages them along the way: “You are really strong. You walk so well. You are doing great. You are a very smart boy.”.<br />
              To assist the little boy on a journey to meets his parents. They may come down seeing unwanted attention and unapproved contact with their child, so be it.

            </p>
            <p> My choice is to be a stranger who leaves the child in front of his parents without connecting with them. No comments. No preaching. No yelling.<br />
              Just a goodbye smile for the kid. Adults can figure it out themselves if they choose to.

            </p>
            <p> Upon reflection, I could almost draw a parallel between such an approach and the overall outcome of RTT session (Rapid Transformational Therapy® that helps people deal with their emotional pain instantaneously and effectively).
              During a session, I can’t change the situation that already took place. I can’t influence, talk to, or punish those who caused pain to the client. But I do help people find a way to cope with what happened, find the power to go over
              it, and leave it behind. Find the way to start a better future and to stop reliving the past.

            </p>
            <p> The next opportunity to walk the talk may not necessarily be at the sledging slope. It may be at a train station or a grocery store. A child might not be three, but seven or ten. But it will arise inevitably.<br />
              And when it comes, my choice is to help young human being understand their own feelings in that difficult moment.<br />
              To help them find the strength to cope with the situation.<br />
              To break the spell of being unseen, unheard, unimportant.
            </p>
            <p> Draco Mom<br />
              Awake Accept Arise.
            </p>
            <p> What is your choice?<br />
              What will you do?<br />
