            <h5>
              Which elements of the workplace environment can trigger a burnout response?
            </h5>
            <p>
              Extensive research conducted by burnout experts Christina Maslach, PhD Psychology, University of California, Berkeley & Michael Leiter, PhD Psychology, Deakin University in 2008, identified key risk factors that can predict job burnout
              at the very early stages. Six elements of the workplace environment can trigger a burnout response: workload, control, reward, community, fairness, and values.
            </p>
            <p>
              A trigger, not the sole and only reason – if you are interested in the origins of the problem, check <a href="burnout-work-trigger.php">"Burnout – your work is just the trigger, not a root cause."</a>
            </p>
            <p>
              <span class="h6">Overload</span> leads to exhaustion. Organizations want employees to be engaged yet seem to expect us to exceed our human limits. The occasional emergency of the extra workload can be expected in most workplaces and
              functions. When overload becomes a chronic job condition, the opportunity to rest, recover, and restore energy and balance is limited.
            </p>
            <p>
              <span class="h6">Community</span> is the overall quality of relationships at work, including trust, support, teamwork, conflict, and politics.
            </p>
            <p>
              <span class="h6">Control</span> and active participation in organizational decision-making increase efficacy and lower exhaustion levels (Cherniss, 1980; Lee & Ashforth, 1993; Leiter, 1992), increasing employees’ energy and health at
              work (Leiter, 2005). Perception of lack of control and influence over own work drains energy and drives disengagement.
            </p>
            <p>
              Insufficient <span class="h6">reward</span> (financial, institutional, or social like lack of recognition from colleagues and managers, but also external stakeholders) devalues both the work and the workers and increases people’s
              vulnerability to burnout (research of Chappell & Novak, 1992; Glicken, 1983; Maslanka, 1996; Siefert, Jayaratne, & Chess 1991, Cordes & Dougherty, 1993; Maslach et al., 1996).
            </p>
            <p>
              <span class="h6">Values</span> are the ideals and motivations that initially attract us to our jobs. This connection between the person and the workplace goes beyond the utilitarian exchange of time for money, prestige, or career
              advancement. When we experience a conflict between our individual and organizational values, we find ourselves trading off between the job we want to do and the one we have to do. It often causes employees to leave the organization
              searching for more fulfilling career opportunities (Pick & Leiter, 1991).
            </p>
            <p>
              <span class="h6">Fairness</span> is the extent to which employees perceive decisions at work as fair and equitable. Research has found that a perceived imbalance between our inputs (like time, effort, and expertise) and outputs (like
              rewards, recognition, advancement) is predictive of burnout (Bakker, Schaufeli, Sixma, Bosveld, & van Dierendonck, 2000; Schaufeli, van Dierendonck, & van Gorp, 1996).
            </p>
            <p>
              Interestingly enough, research on procedural justice (Lawler, 1968; Tyler, 1990) has shown that what matters more is the fairness of the process than with the favourableness of the outcome.
            </p>

            <h5>
              Fairness and the boss
            </h5>
            <p>
              Employees who perceive their supervisors as fair and supportive are less susceptible to burnout and are more accepting of significant organizational change (Leiter & Harvie, 1997, 1998). Please note, I am far from pointing fingers at
              managers whose team members experienced burnout. Unless it is a notorious occurrence in your team, then do have a long, hard look in the mirror.
            </p>
            <p>
              We are talking about human perception, which by no means is objective or based on complete data. It’s lenses of our employees. The good news is perception can be changed. It’s not easy, often not comfortable, but not impossible.
            </p>

            <h5>
              The tipping point
            </h5>
            <p>
              A longitudinal study performed by Maslach & Leiter (2008) revealed interesting findings suggesting that, among these six factors, fairness can play a critical role in developing burnout. If employees who were already experiencing
              exhaustion or cynicism (considered potential early warning pattern) perceived problems with justice in the workplace (such as favoritism, unjustified inequities, or cheating), they were likely to develop into burnout over time.
            </p>
            <p>
              On the other side, people with the same early warning pattern (of either exhaustion or cynicism) who were not experiencing injustice even were likely to fuel up their energy over time and turn into an engagement.
            </p>
            <p>
              The research continues, but some suggest that fairness may constitute a primary tipping point— either the first, or only, or most important one.
            </p>

            <h5>
              Does your employee engagement survey measure the perceived level of fairness in your organization?
            </h5>
            <p>
              Do you ask your employees if they feel processes are transparent and equitable?
            </p>
            <p>
              Do you check if your overloaded co-workers perceive their boss as impartial and just?
            </p>
            <p>
              Do you feel treated fairly in your workplace?
            </p>
            <p>

              Don’t ask if you are not ready for the truth.
            </p>
