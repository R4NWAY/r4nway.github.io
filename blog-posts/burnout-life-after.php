            <h5>
              Gradually. Then suddenly.
            </h5>
            <p>
              “Dave Flynn, Director of Internal Audit, has decided to leave the organization effective November 30th. Dave decided to pursue his career outside of our organization. We will announce his successor soon. We are very appreciative for
              all the hard work that Dave has done for us over the last seven years, and we wish Dave the best of success in his future endeavours.”
            </p>
            <p>
              Nobody seems to be surprised seeing this organizational announcement. It was just a question of time. Dave made himself known widely in the organization as a talented, hard-working, highly engaged leader and financial expert. It was
              visible for all: his reports, his stakeholders, his boss, and the HR team that over the last year, Dave struggled and was on a slippery slope of burnout.
            </p>
            <p>
              And just like Mike Campbell, the character in the Hemingway’s novel “The Sun Also Rises”, “Gradually, then suddenly”, Dave hit rock bottom. He was struggling with a heavy workload but tedious job, limited development opportunities,
              lack of recognition. After countless discussions with his boss and HR, after numerous internal applications for roles he seemed to be a great fit for, Dave was rejected for the sixth time in a row. The only thing that was given to him
              was just a lousy explanation. He could not handle that anymore. He has been sick and tired for the entire last year. Now he literally couldn’t get up from his bed. He went to the doctor. Diagnose: burnout.
            </p>
            <p>
              How Human Resources usually handle burnout and simple way to do it better:
              <a href="burnout-sloppy-outsourcing.php"> "Burnout. A quick and simple way to turn sloppy outsourcing into effective HR practice."</a>.

            </p>
            <p>
              Please note, while the stories of my clients are true, their names and identities have been changed to protect their privacy. Thank you for understanding.
            </p>
            <p>
              Once we discovered the root cause of his burnout in the Rapid Transformational Therapy® session (link <a href="burnout-work-trigger.php"> here</a>) and dealt with “unfinished business” from his past that drove him to run out of fuel at work, I asked Dave whether he plans to
              return to work.
            </p>
            <h5>
              Employee perspective: return is career suicide.
            </h5>

            <em>
              “There’s nothing for me to come back to. It doesn’t matter that I am now ok, stronger, more assertive, have a better understanding of myself, my advantages and limitations, and so on. It doesn’t matter that I have the same skills and
              talents I had just two months ago. There are no opportunities for me there whatsoever. My boss and all higher-ups think I’m weak. If they haven’t given me another job so far, there is no way they will give me anything now. I’ll
              be stuck in the same crap. I am a lost case for them. My team thinks I’m emotionally unstable. I don’t want pity from my colleagues tiptoeing around me. It would be awkward if I got back. It’s better for them and me if I leave.”
            </em>
            <br />
            <br />
            <p>
              I never influence my clients’ decisions about their lives. It is essential that people, who have just found and rebuilt their strength, voice, self-esteem, and self-confidence, are in control of their destiny. Where, how, when, with
              whom to live, work, share, break, forgive or not, etc. – it is their call. They are capable and confident to make these decisions.
            </p>
            <p>
              At the same time, it is striking that almost all clients who come for help with burnout decide to leave the organization that
              triggered this condition. Burnout is one of the best predictors of leaving the organization (<a href="https://www.researchgate.net/publication/270466178_Returning_to_work_after_suffering_from_burnout_syndrome_Perceived_changes_in_personality_views_values_and_behaviors_connected_with_work"> Barak, 2001;
              Goodman & Boss, 2002</a>). Even if, on their end, they now could handle the same situation much better, they see no future in their recent workplace.
            </p>
            <p>
              And trying to be as objective as possible, they are probably right.
            </p>
            <h5>
              The new beginning instead of mark of disgrace
            </h5>
            <p>
              In the new organization, they can start with a clean slate. With energy, with all their professional experience and this recent experience that enriched them, with no prejudice, no pity, no awkwardness. When you return after burnout,
              you are not back in the same place even after <br />
              a relatively short break (a few weeks). From a career perspective, you move backward. You know you’ll be watched closely, even if you have proven yourself over the years. You presume the
              praise will be out of sympathy and because of “your state”, not because you did something that deserves recognition. You envision that your name will have a question mark next to it during succession planning conversations, doubting
              whether you have enough resilience and emotional stability to lead on the higher level.
            </p>
            <p>
              Why on earth would a smart, industrious, ambitious person decide to come back to be stigmatized?
            </p>
            <p>
              Dave was right.
            </p>
            <h5>
              Is letting Dave leave the best for the organization?
            </h5>
            <h5>
              The logic: economically not
            </h5>
            <ul>
              <li> Dave’s boss was losing an experienced and reliable director – somebody who was doing his work well and lead a busy team. There was no internal successor ready to step up. </li>
              <li> As in many European countries, an external search would take a few months, followed by a standard three-month notice period. In a best-case scenario, replacement would be there in half a year and could be fully productive after a
                few months of onboarding. </li>
              <li> For at least the next nine months, the boss had to split Dave’s job, cover part of it himself, organize an expensive interim consultant to pick up the rest, and on the top of his direct reports, lead capable but the junior team of
                Dave. </li>
              <li> Direct costs of recruitment, onboarding, and interim consultant are always higher than if the organization keeps experienced performing employee. </li>
            </ul>
            <p>
              No, letting Dave leave is not the best for the organization. But as much as the corporate world always tries to convey the objective, logical, calculated, even detached image, human beings are driven by emotions.
            </p>
            <h5>
              The reality: it is easier
            </h5>
            <p>
              Ironically, if Dave leaves, it is just way easier for the organization.
            </p>
            <p>
              People in the corporate environment are used to deal with the extra workload, limited resources, the pressure of goals, deadlines, and senior stakeholders. It is not comfortable, but it is very familiar. Besides, these excessive
              workloads, pressures, unrealistic targets are external. We can complain about them, criticize them, laugh about them, but they are not ours. They are not us.
            </p>
            <p>
              But looking in the mirror and recognizing that something we have or have not been doing made somebody feel unimportant, undervalued, overwhelmed, etc., is way less familiar and less comfortable. That is extremely tough. Admitting that
              would require not just courage but also time to address that. And we are all so busy…
            </p>
            <p>
              It is easier to choose the narrative “It was him, it wasn’t us. He was overly sensitive, not resilient enough, had no coping skills.”. It is easier to replace a person, despite its high cost.
            </p>
            <p>
              Time, effort, and money are easier to trade.
            </p>

            <h5>
              But what else do we trade-off in this deal?
            </h5>
            <p>
              I would argue the opportunity to make our leaders better and our organizations stronger.
            </p>
            <p>
              As we let them leave quietly, we hope the problem vanishes with them. (if “wishful thinking” was a competency we would score “exceeding expectations”;) We turn a blind eye not only on the departure of a bunch of amazingly
              hard-working, dedicated, caring employees. Extremely loyal co-workers who are passionate about what they do, produce results, support their teams, and act with integrity.
            </p>
            <p>
              We turn a blind eye to the departure of people who could help address organizational and personal issues in our workplace before it is too late. Too late for the rest of us and too late for the organization.
            </p>
            <p>
              Those who undergo burnouts are like canary birds used in coal mines to detect toxic gases before they hurt humans. These animals are more sensitive to colourless, odorless carbon monoxide and other poisonous gases than humans. If the
              animal became ill or died, that would give miners <br />
              a warning to evacuate.
            </p>
            <p>
              We need our canary birds before our organizations become too corrupted, too hostile, too unjust, too immoral.
            </p>
            <p>
              Okay, I still want to look myself in the eye, so what could my organization do to make things better?
            </p>
            <h5>
              Destigmatize burnout – experts’ recommendations
            </h5>
            <p>
              Several long-term strategies aim to demystify and destigmatize burnout. Most base on lots of communications:
            </p>
            <ul>
              <li> mental health policies, </li>
              <li> mental health days, </li>
              <li> stories of senior leaders sharing struggles with mental wellbeing, </li>
              <li> flexible working solutions to enable better work-life balance. </li>
            </ul>
            <p>
              They sound great. Somehow, we just don’t hear that companies massively try to implement them.
            </p>
            <p>
              Many of us experience a gift of flexible working arrangements in response to the announced pandemic situation, not out of care about employees’ mental wellbeing.
            </p>
            <p>
              Just as anti-bullying policies at schools, mental health policies don’t solve the problem they are meant to fix. (I will write about reasons why and how we can equip our children to protect and defend themselves better soon.)
            </p>
            <p>
              Communication about mental health is needed, but who really should do it (leaders? HR? experts? internal? external?), how much, how intense is not so straightforward. That’s why too often, companies that decide to go this route
              announce celebrating Mental Health Awareness Month & occasionally distribute leaflets on each desk about how important it is to eat healthily, sleep well, practice mindfulness, and exercise more. (These are excellent tips, but trust
              me, that’s not what helps my clients dig themselves out of the burnout hole – you can read   <a href="burnout-work-trigger.php">here</a>.)
            </p>
            <p>
              Many experts suggest senior leaders should speak up about their mental wellbeing struggles. I bet it would help destigmatize burnout. That could be a silver bullet. I just can’t imagine it is possible at all, because:

            </p>
            <h5>
              1) “This is a man’s world”
            </h5>
            <p>
              Please bear with me; I don’t intend to open a dispute about diversity whatsoever. But to attempt to understand the situation from a psychological perspective, we can’t ignore the fact that most medium and big organizations are run
              predominantly by men.
            </p>
            <p>
              If you are a woman, ask your spouse, brother, father, male colleague how usually men react when they hear their male colleague suffers depression, burnout, anxiety, panic attack…?
            </p>
            <p>
              Answer:
            </p>
            <ol>
              <li> Most often, they don’t react as they don’t hear. Because it’s not the thing, men typically share with their buddies. Most men feel embarrassed to be in such a situation and don’t want to be excluded, stigmatized, seen as a
                failure, defeated, and weak. They don’t share. (Well, they do on anonymous depression forums seeking help, guidance, and support, but they don’t usually mention that to people around them, especially other men.)</li>
              <li> Most of the time, men don’t even want to hear. It is so unfamiliar to them. So uncomfortable. They have no idea how to react. They are not used to talk about it, ask about it, offer help or a shoulder to cry. They don’t know what
                to do. </li>
            </ol>

            <p>
              Given that men run most organizations, how can they suddenly open up and break such an unspoken manly rule?
            </p>
            <p>
              If most men don’t disclose, I am not sure women would do themselves a favour sharing their struggles.
            </p>
            <p>
              There are many books written about why men keep their emotions inside. It’s not just the last few centuries that we teach boys to be strong, “suck it up” or “fight back”, and that crying, showing feelings, and asking for help is a sign
              of weakness.
            </p>
            <p>
              For thousands of years, men have been hunters. Suppose a hunter revealed his doubts about potentially an unsuccessful hunt that could cause his wife and the entire tribe to panic. Meanwhile, women were free to share their fears and
              emotions with each other, which was therapeutic and healthy for them and the tribe.
            </p>
            <p>
              As a consequence of thousands of years of our evolution, women tend to have better support systems—friends who’ll listen as long as needed (days, weeks, months) without judgment, offering ideas and emotional support. Men typically are
              more focused on “fixing” and offer advice on what to do rather than “dwelling” on uncomfortable emotions. And they expect you to be “fixed” by the next time they see you. But there is no simple one-sentence-tip to burnout, depression,
              or grief. Hence men often are left alone with their suffering.
            </p>
            <h5>
              2) This is a war zone too
            </h5>

            <p>
              For thousands of years, men have also been warriors. I think it is fair to say that the execs leading organizations (regardless of gender) are at war. They battle to win customers’ hearts, minds, and wallets. They fight to win the war
              talent. They combat the competition on the market. And they have personal rivals waiting for the smallest misstep.
            </p>
            <p>
              A warrior on the battlefield doesn’t share secrets that would make him vulnerable for the sake of comforting his hurt companions. That’s not how you win wars.
            </p>
            <p>
              Many great leaders are not afraid to be vulnerable. They open up about their childhood struggles, share anecdotes about poor leadership choices early in their careers, take responsibility for lay-offs in times of crisis. But it is
              different than admitting, “I struggled with depression, I suffer anxiety, I used drugs/alcohol/porn to deal with stress, or I experienced burnout.”
            </p>
            <p>
              As powerful and liberating as it could be for an employee who shares the struggle, it’s not the messaging market and shareholders ever want to hear.
            </p>
            <h5>
              So perhaps it is not right to ask organizations to address burnout differently?
            </h5>

            <p>
              Maybe we expect too much from a structure or a system whose primary objective is to make a profit (or stay within a budget if we consider public schools or hospitals).
            </p>
            <p>
              Perhaps letting people leave is a fair deal?
            </p>
            <p>
              We know what we are losing: employees with all the qualities they had before: skills, certifications, talents, unusual work ethics, ambition, and drive, people who now have higher self-awareness, grown self-confidence, resilience,
              strength, who also have more voice, more courage, more power. And their canary-bird sensitivity is now toughened by a sense of self-worth and assertiveness.
            </p>
            <p>
              And we know what we are getting: as we turn a blind eye and let great people quietly leave somewhere else, we let people in who left somewhere else for a similar reason. They just chose not to share it, and we decide not to explore it.
              And as a headhunter calls to check Dave’ references, we gladly say “reason for leaving – limited career opportunities”. And we backfill Dave’ role with somebody whose “reason for leaving” is “lack of advancement options in last
              company”.
            </p>
            <h5>
              Perhaps a curtain of silence that has been rung down on burnout is the best solution for all? What do you think?
            </h5>
            <p>
              For me, the answer to the question is more complex than our first emotional reactions.
            </p>
            <ul>
              <li> I wholeheartedly understand Dave’ concerns. </li>
              <li> I relate to managers’, teams’, and HR dissonance: uncomfortable but familiar vs. unfamiliar and potentially bitter. </li>
              <li> I genuinely believe canary birds are invaluable, and we should take care of them. </li>
              <li> And gosh, I expect our leaders to be-warriors that fight fiercely and win. </li>
            </ul>
            <p>
              I wish I had a sword to cut this Gordian knot with one simple, bold and ingenious solution. Wishful thinking again, I know.
            </p>
            <p>
              But as I don’t own a sword, I follow what one deep thinker and a warrior himself recommends for seemingly “unsolvable” problems of a big scale: “seek out your own ideal world”.
            </p>
            <p>
              The true “ideal” would be “the world without a burnout”, but probably to face this discussion, we need to be ready to take some deliberate measures on how we raise the next generations and how we operate organizations. So, for now, I
              look for a smaller version of “ideal”. I asked a few of my cured out of burnout clients what “the ideal world post burnout” could look like. What would have to happen so that they seriously considered coming back to their old workplace
              and had a trust they can flourish? Here’s what they said:
            </p>
            <h5>
              It takes two to tango.
            </h5>
            <p>
              And it takes an army of kind-hearted people to change the way we think about burnout.
            </p>
            <p>

              There are three elements that in combination would make the risk of return worthwhile.
            </p>
            <h5>
              I. A precondition: trust
            </h5>

            <p>
              Every single person I discussed this topic with said it is a critical factor.
            </p>
            <p>
              “At the time when I am running low on fuel before I crash, I must feel my boss and my HR person trying to understand my situation and that they genuinely care. Without that, I can’t trust that things will get better when I am back.”
            </p>
            <p>
              Sadly, quite often, they did not feel supported on this journey. Some of them decided not to reveal the struggle they went through, questioning the other side’s intentions or confidentiality. Some felt their boss would worry more about
              job undone and the HR about quick replacement than their health.
            </p>
            <p>
              And it doesn’t really matter if they are correct or are these “just” their assumptions. Because in either case, the trust was not there, and it was too late.
            </p>
            <p>
              But let’s look at the cases when it’s not too late. When trust and genuine concern are there.
            </p>
            <h5>
              II. Practices established before my burnout
            </h5>
            <p>
              “Nobody ever in my company talked about burnout (or depression or addiction or any other mental problems). I felt I am the only one experiencing it. If now I come back and they suddenly start an information campaign, I will feel
              exposed. It’s not the attention I want after this entire experience.”
            </p>
            <p>
              Articles, videos, posters, and leaflets are excellent sources of info, but it is such a personal issue that people want to connect with real people like them and near them. They want to talk to Dave from the 2nd floor from Internal
              Audit, not read about John Smith from Cleveland.
            </p>
            <p>
              To whom would they like to hear?
            </p>
            <ul>
              <li>Colleagues, sharing their burnout stories and how they are now thriving, became more resilient, more in control of their lives, and fulfilled in their careers.</li>

              <li>The expert (internal or external) with authority in mental wellbeing topic. Just please, let’s not cut down mental health to a session of mindfulness. Mindfulness can work wonders but is not a silver bullet for all problems.</li>
              <li>As in big organizations, our senior leaders-warriors are at wars, maybe the retiring or retired “high-rank generals” could talk about their burnout/depression/anxiety struggles? The only thing they have at stake at this moment is
                their image. They want to be remembered in the glory of their past achievements. But we know there is neither sentiment nor memory of past achievements in the corporate world anyway (yesterday is already a history), so perhaps
                inspiring the next generations is the way of making yourself truly memorable?</li>
            </ul>
            <p>
              These conversations (not lectures) don’t have to be frequent or lengthy. Lunch & learn format is feasible. But making time for such meetings sends the clear signal that people’s health and wellbeing matters in this organization.
            </p>
            <h5>
              III. Upon my return…
            </h5>
            <p>
              The third piece of the puzzle is addressing poisons our canary birds sensed. And it requires a skilled facilitator and mature participants:
            </p>
            <ul>
              <li> The manager who is brave enough and humble enough to put their ego to the pocket and invite the entire team to discuss which leadership practices and aspects of organizational culture are dispiriting. </li>
              <li> The team that will contribute to open and constructive discussion, without downplaying (“all is great, we don’t have any problem”) or taking a chance to vent. </li>
              <li> The HR who can help facilitate such session and support the change afterward. </li>
              <li> And finally, the employee will share their conclusions: underlying beliefs, work-related convictions, and behaviors, which led them to run out of fuel. (For example, “I realized I am a helpful scout who always agrees to help
                others when asked, but I struggle to ask for help myself”) </li>
            </ul>
            <p>
              Speaking up confidently about your insight shows to your team you have fully recovered.
              It also takes off the pressure from your boss, who, in common understanding, is a cause of your burnout
              (which is not entirely true – check <a href="burnout-work-trigger.php"> "Burnout
              – your work is just the trigger, not a root cause"</a>.)
            </p>
            <p>
              You’d be surprised how many people, after complete recovery, would be confident to do it. It just really needs to be the third and not the first thing we do.
            </p>
            <h5>
              It is not a five-minute-thing!
            </h5>

            <p>
              It seems like a lot of effort. It looks serious.
            </p>
            <p>
              It is serious, indeed. Burnout (just like depression, anxiety, phobias, etc.) is serious for a person affected by it. It is serious for their family too.
            </p>
            <p>
              It is not simple, and it is not easy.
            </p>
            <p>
              Is it worth the effort?
            </p>
            <p>
              Is it worth the effort to help our employees and our organization? Is it worth trying to ensure we are not putting salt in old wounds but authentically help people thrive, realize and expand their potential and make work a
              complementary element of our accomplished lives?
            </p>
            <p>
              Or is letting them leave silently the best way for all?
            </p>
            <p>
              Let me know what the answer for your organization is.
            </p>
            <p>
              I also look forward to hearing your ideas, experiences, and please, please, please share if you discovered a sword to cut this Gordian knot.
            </p>
            <p>
              Draco Mom<br />
              Awake. Accept. Arise.
            </p>
