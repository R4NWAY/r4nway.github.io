            <h5>
              Burnout. Google. Image search.
            </h5>
            <p>
              Most frequent outcome: a person is collapsing on their desk, quite often tearing their hair out. Sometimes spiced up by fumes steaming out of their ears, flames, or a mad look in their eyes. They must be rare animals, as I haven’t seen
              even one like that over my entire career.
            </p>
            <h5>
              How do they really look like?
            </h5>
            <p>
              For twenty years I have worked with people who experienced burnout on both sides of the fence: arm in arm in the corporate world as HR professional and eye to eye as their therapist. One thing I can say: they do not look crazy at all.
              Right on the contrary – most of them look and indeed are very smart, organized, effective, and efficient. They often work extra hours, go the extra mile, work their fingers to the bone. They are engaged. They genuinely care about their
              work and the organization they work for. Until they burn out.
            </p>
            <p>
              They look like high potential superstars; they look like capable, reliable core players; they look like solid and committed high performers that serve and save organizations day in and out. Their colleagues value and respect them as
              they are pleasant and likable, and they deliver on their commitments. They perform and outperform.
            </p>
            <h5>
              A good reason for employers to care.
            </h5>
            <h5>
              Definition
            </h5>
            <p>
              By “burnout,” I don’t mean the situation when an employee hates his job, thinks his boss is a jerk and considers his colleagues annoying. That’s also a stressful position to be in, but it doesn’t necessarily imply one is at risk of
              burnout. I refer to people experiencing all three dimensions: exhaustion, cynicism, and professional inefficacy as a prolonged response to chronic emotional and interpersonal stressors on the job (it is the most commonly used
              definition, crafted by <a href="https://www.researchgate.net/profile/Michael-Leiter" target="_blank" rel="noopener noreferrer">Michael Leiter</a> & <a href="https://maslach.socialpsychology.org/"target="_blank" rel="noopener noreferrer">Christina Maslach</a>).
            </p>
            <h5>
              Diagnose: burnout. Cause: work.
            </h5>
            <h5>
              I disagree that this widely accepted answer is the sole and main root cause of the problem.
            </h5>
            <p>
              If it would be, wouldn’t all people experiencing the same working conditions be affected? There must be some individual factors making some people predisposed more than others.
            </p>
            <p>
              There is a lot of research data available on the internet, where you can explore findings of various professional groups (nurses, doctors, athletes, lawyers, etc.). In most cases, they indicate a low or moderate correlation of some
              personality traits with burnout (mainly neuroticism – positive and extraversion – negative). I don’t want to derive the discussion into definitions and tools measuring these different aspects of personality and how we develop them (all
              available at your fingertips).
            </p>
            <p>
              I think psychological labels neither explain how we got to be a certain way nor help us deal with a potential problem. Too often, labels (be it neuroticism, ADHD, depression, or anxiety disorder) stigmatize, put a person in a victim
              mode, and restrict their behaviors, dreams, aspirations, lives.
            </p>
            <h5>
              I will share a few stories (out of many) from both my HR and therapy practice to reveal a common but not widely known origin of burnout.
            </h5>
            I hope to inspire reflection in those of you who experience burnout like symptoms to encourage you to seek professional help sooner and in the right place to improve your life fundamentally and forever. Because there are proven ways to
            understand where your issues came from, address them, and heal.<br />
<br />

            <h5>
              Burnout from the HR perspective
            </h5>
            Please note, I respect everyone who comes to me for help. Many are working toward a fresh start in life. So, while their stories are true, client names and identities may have been changed to protect their privacy. Thank you for
            understanding.<br />
            <br />
            <h5>
              Dave. Always Great. Never good enough to be promoted.
            </h5>
            <p>
              A very well educated, multilingual, and an incredibly successful Internal Audit Director in a big multinational. Always delivering in his job and being regarded as a great talent. After almost seven years in the role, being loaded with
              heaps of work challenging in volume, not really stretching his mental potential, he started to address his physical exhaustion and intellectual boredom. He talked with his boss, connect with key stakeholders in the company, got support
              from HR. Dave kept applying internally for interesting roles that he seemed to be an excellent match for. He still worked extremely hard, delivered during long days, overnights and weekends, and always put lots of effort into
              supporting and developing his team. He believed in the company he worked for and he saw his future there. He got turndowns each time role he applied. He felt betrayed.
            </p>
            <p>
              Next to being physically exhausted from extremely intensive yet repetitive work, he became bitter, disappointed, angry at his bosses, frustrated with stakeholders. He started to doubt his high potential status (why wasn’t he offered
              opportunities as he clearly had mental capabilities and support of senior stakeholders?) and became very sour about his leaders and the company itself. He started to suffer from headaches, stomach cramps, nausea, lost his appetite
              despite being a very fit person.
            </p>
            <p>
              Dave sought medical help a few times, and next to hearing “burnout” he got some days off along with a piece of advice to rest, sleep, relax, and live healthier. He ate healthy and regularly exercised his entire life. If only he could
              relax easier and sleep better, that would be the first thing he would do. After studying potential side effects of offered drugs, he refused to take medication that would help him sleep but would not solve his problem. After a few
              months of back and forward, he left the company. It was the fourth time in his impressive career when he took such a decision after going through a similar cycle.
            </p>
            <h5>
              Annemarie. Lost enthusiasm. <br />
              Why reckless hyenas keep stealing the credit for my sacrifice?
            </h5>
            <p>
              Early in her life, she found her vocation as a teacher. Enthusiastic, patient, and loved by her pupils, she always put lots of effort to inspire and explain the world to her Early Years classes. Throughout the years of her teaching
              career, she worked hard to advance her teaching credentials. She was dedicated to her calling and extremely hardworking.
            </p>
            <p>
              As a highly transparent person and a firm believer in meritocracy, she never wanted to be involved in internal politics. She refused to use informal networks to advance her career or get any privileges. Some of her more political (or
              maybe just jealous of her successes) peers often took advantage of her. Her schedules were often less than ideal, with breaks spread throughout, making her working days significantly longer than other teachers. She often got involved
              in voluntary initiatives, like organizing support classes for kids falling behind, arranging occasional children performances, or school involvement in locally relevant social actions (collecting toys for an orphanage, organizing
              charity for an animal shelter, etc.). And way too often, although she was the mind, the heart, and the hands behind these initiatives, officially in front of parents, school leadership, and governing education institution, the credit
              was given to somebody else. Annemarie concluded that her dedication, sacrifice, and honesty did not pay back.
            </p>
            <p>
              She saw the injustice more and more, and gradually she started to withdraw herself from any extra activities. With time she began to refuse to borrow her additional tools and presentations she had developed herself and found excuses
              when asked to be a replacement teacher. She became resentful of her selfish political colleagues, the unfair headmaster, and the deceitful administrative assistant. The optimistic and energetic social butterfly has turned into a quiet
              mouse sneaking in and out of work, avoiding contact with other teachers, and waiting for the afternoon to arrive, just to lock herself at home, feeling crashed, deceived, and lonely. The sense of failure overshadowed the joy from kids’
              progress and affection. Annemarie lost her heart to teach.
            </p>
            <h5>
              Monika. Keep giving it all you’ve got. Keep sucking it all up. <br />
              Undervalued, insignificant, belittled with tons of “thank you” cards.
            </h5>
            <p>
              Warm, patient, and caring midwife in her mid-thirties. Extremely competent, but also very empathetic and flexible. Monika was busy working in a 12-hour-shifts system in a public hospital. Regardless of the shift, she was accepting
              calls from her clients at any time without limits. Being a mother herself, she understood very well how worried and anxious mothers to be are. She was agreeing to change or cancellation of appointments with short notice. She was doing
              everything she could to make the experience safe and “right” for her clients. No wonder her clients adored her.
            </p>
            <p>
              Monika received lots of postcards with newborns from grateful mothers, boxes of chocolates, and occasionally other forms of gratitude. Yet, despite a super busy schedule and emotionally loaded work, her financial situation was very
              tough. And she was exhausted. Moments of joy were frequent but short. Limited time for her own family, exhaustion after high-on-adrenaline long shifts, and the face of her 6-year-old son when she had to repeat “no, we can’t afford
              that” were everlasting.
            </p>
            <p>
              The atmosphere at work was also far from ideal. It was almost a norm that some doctors would make insensitive or belittling comments. Sometimes they were said in their presence, “Great that somebody can hold a patient’s hand when we do
              the real work.”. Sometimes they hit Monika’s ear when passing by the doctors’ office: “In middle ages, midwives were burnt as witches.”.
            </p>
            <p>
              Initially, she fiercely discussed these with other midwives and nurses. After complaining to the head physician, they heard “Ladies, don’t be so touchy. Surely it was a joke.” and “Everybody sometimes says something they haven’t well
              thought through when they are as tired as doctors are.”
            </p>
            <p>
              With time she learnt to shut down to these, as she called them “emotionless, corrupt, shilling for big pharma narcissists”. She stopped seeing all medical professionals as people following their calling. She started to disconnect from
              her clients as she felt they did not respect her boundaries. A box of chocolate could not compensate adequately for her time, effort, and dedication. She felt that self-centered clients and colleagues took advantage of her open heart
              and genuine intentions. She cried quietly every night, and she started to feel nauseous before going to work. Paradoxically, it is not always easy to go to the doctor when you work in a hospital. Especially if the only thing you want
              to say is “I am sick of this job”.
            </p>
            <p>
              It was Monika’s deliberate and noble life choice to assist women in their most life-changing moments, and after 14 years, she was regretting it.
              <h5>
                Common denominator?
              </h5>
              <p>
                Dave, Annemarie, Monika. Three different people, three different stories.
              </p>
              <p>
                What do they have in common?
              </p>
              <p>
                Ambitious? Dedicated? Conscientious? Keeping themselves up to very high standards?
              </p>
              <p>
                Indeed, yet, not all industrious, diligent, aspirational employees experience burnout.
              </p>
              <p>
                Caring? Kind? Not assertive enough?
              </p>
              <p>
                Fair point, but again, not all warm-hearted and agreeable people end up with a pit in their stomach when just thinking of their work.
              </p>
              <p>
                All chosen workplaces that were not fulfilling their needs (fairness, recognition, career advancement, etc.) and respecting their boundaries?
              </p>
              <p>
                Correct and… without validating hostile work cultures, let’s be honest, after 10+ years of work in any profession, we all can say we’ve experienced that somewhere in the course of our careers, haven’t we? Most of us did not end up
                burned out from it. So why some people do?
              </p>
              <h5>
                From the therapist’s couch
              </h5>
              <p>
                Let me reveal what came up in our burnout focused RTT (Rapid Transformational Therapy®) sessions with Dave, Annemarie and Monika.
              </p>
              <p>
                Please note I am sharing the stories of selected three individuals here. My conclusions though are based on many more people I worked with on this problem and insights from other therapists who share my observations.
              </p>
              <h5>
                Little boy called Dave
              </h5>

              <p>
                In many ways, Dave was an ordinary Irish boy. He lived in a small town with both parents, an older brother and a small sister. Middle class, both parents worked to provide for the family, no addiction or acts of violence in the
                family. A bit cold father who believed his role was to toughen the boys and a loving mother who gave all the children lots of warmth, compensating for her emotionally distant husband. The mother who, like many moms, dreamt about
                better lives for her kids. The mother who used to encourage her children to study hard, be the best they can be, and who applauded each of their educational success.
              </p>
              <p>
                Not bad at all; many kids are less lucky with their parents.
              </p>
              <p>
                Yet, in this, quite average for its time and place environment, details shape Dave into whom we see 30 years later. Dave’s one-year older brother, Thomas, was a troublesome kid. He got involved in fights, skipped school now and then,
                lied too much, and kept his mom worried. She was very busy with a baby girl and did not have enough time to keep a close eye on boys.
                She often cried, worried about Thomas’s future. She used Dave as a role model, hoping Thomas would
                follow his example and get better at school. It didn’t work.
              </p>
              <p>
                Dave suffered seeing his beloved mom in pain and crying. He was a good son; he made her proud. He was the best student consistently since primary school through university. His mom was proud of Dave and worried about the older one.
                It deeply upset little Dave to see how irresponsible, hurtful, and selfish his brother was (to be accurate, Thomas was just age-appropriate, but it’s hard to conclude when one is 8 or 9 himself). It broke Dave’ heart to see that he
                couldn’t wipe off his mom tears forever. But he could compensate for his brother. And he did. Every day.
              </p>
              <p>
                At the same time, Dave’ father didn’t seem to recognize and appreciate Dave’ efforts and academic achievements. “It would be better for you if you played football with other guys more often”, he commented.
              </p>
              <p>
                Again, not really a drama. No trauma, no abuse, no sensation. But it is not the drama that matters. What matters are thoughts and beliefs that a few year-old Dave has formed by living this life day in and out.
              </p>
              <ul>
                <li> I need to work hard, give it all and be the best as it is the only way to make my mom proud and happy for a while. </li>
                <li> No matter how hard I try and how good I am (winning school competitions or academic achievements), I can’t take the suffering away from my mom. </li>
                <li> No matter how hard I try and how good I am, I can’t make my dad proud, and hear “you are a great boy, you are a great son, I am proud of you, I love you”. </li>
                <li> All that must mean “I am not good enough. I am not good enough to make my mom happy. I am not good enough to make my dad proud. I am not good enough to deserve my dad’s love and affection and be accepted the way I am. I am not
                  good enough.” </li>
              </ul>
              <p>

                These were not conscious conversations Dave had with himself. These were unquestionable facts, the only reasonable explanations formed by the young boy growing up with such a daily experience.
              </p>
              <p>
                And with such a “program” Dave goes through his, what looks like very successful life. He studies more and more, works harder and harder, proves himself more and more, yet… still does not fill enough. Working his butt off to deserve
                recognition, attention, promotion. Dreaming to have a more balanced life yet depriving himself of it, hoping that somebody (his boss, his team, organization) would fill the empty space in his heart. Tough. Organizations are not good
                at, and not really meant to be filling gaps in hearts.
              </p>
              <p>
                Understanding is power. Dealing with Dave’s “gap in the heart” took two meetings.
              </p>
              <h5>
                Little girl called Annemarie
              </h5>
              <p>
                She grew up in a Dutch town as a single child of two parents, renowned journalists. The house was always full of politicians, celebrities, journalists, local artists. Annemarie loved reading and dreaming, and since a very young age,
                she was great at playing by herself. Her parents didn’t need to guard or entertain her much. They gave her a lot of freedom as she was smart, cautious, and very self-sufficient. Freedom sounds great for a teenager, but it wasn’t that
                delightful for a 3,4,5-year old girl who craved parents’ attention, time, warmth. The reality was that they didn’t have much time for her, so she learnt to take care of herself early on. She accepted the status quo and soon stopped
                asking for attention.
              </p>
              <p>
                Her parents provided her with food, nice clothes, stimulating toys, and books. She was not a neglected child in a common understanding. Surely, many kids have it much harder.
              </p>
              <p>
                Unexpectedly, when Annemarie was 9, her parents adopted another child, a boy. The little girl could not understand why they would do it. Undoubtedly, they had no time for one child… How will they manage with the two?
              </p>
              <p>
                Even more shocked she was when it turned out that her parents could give a lot of time and attention to a child. Not to her, though. Her foster brother was going through quite typical troubles with bonding, lying, disregarding
                authority, and had problems adapting in kindergarden. Annemarie could not understand why she, such a polite, disciplined, smart, and obedient girl, could not deserve the attention the new and unruly family member was receiving. She
                was heartbroken.
              </p>
              <p>
                Clearly, for a child, she was missing something her new brother had. Surely, there was something wrong with her. She was not good enough. She was not lovable. She didn’t deserve attention and time. How could a child understand and
                explain herself the fact she was deprived of love, denied attention, warmth, and connection despite doing everything best she could? She became even more silent, even less demanding, invisible.
              </p>
              <p>
                And again: no abuse, no addictions in the family. From the outside, it was a good, quite fascinating, upper-middle-class family. From the outside, one could not see one very, very unhappy child.
              </p>
              <p>
                Years later, she became a teacher to ensure that children she would contact would not suffer a lack of attention and would have somebody who always takes time to explain to them the world. And again, she could not understand how with
                all her efforts, with all she gave to kids, to school, to be the best version of herself for others, she is betrayed, not recognized, … not loved.
              </p>
              <p>
                Linking the past and recent experience made her realized she carried these feelings and questions throughout her life. She realized that her interpretation of her childhood was the only one available for her back then. But now, she
                had more options. She could choose to see things differently.
              </p>
              <p>
                She realized it was not her missing something. She was perfect. She was an absolutely lovable girl many parents dreamt of. Annemarie’s mother and father were self-centered and did not deserve such a smart and easy-going daughter as
                she was. It was her parents that failed, not her. Burnout was gone almost instantly. She reclaimed her voice, her power, and decided to kick assess and reclaim the credits she was due. Her heart grew bigger and stronger.
              </p>
              <h5>
                Little girl called Monika
              </h5>

              <p>
                Monika was born as the first child out of three in an average working-class family in Eastern Europe. The circumstances of place and time forced both her parents to work shortly after she was born. They could have either given their baby to the
                nursery since the age of 12 weeks or leveraged retired grandma. They chose the second option. There was just a small problem – grandparents were not living in the same town, daily trips would not be possible. They decided they will
                be visiting their first baby over the weekends. Monika grew up with her grandparents for the first few years of her life.
              </p>
              <p>
                Similarly, like Dave and Annemarie, when asking “What was your childhood like?” at first I heard, “All fine, average family, three kids, normal parents, providing for us; we were taken care of, no alcohol, no violence in the
                background, nothing special.”.
              </p>
              <p>
                But there was something special.
              </p>
              <p>
                Monika mentioned she had no early childhood memories from before the age of 7. That is quite unusual.
              </p>
              <p>
                And during the session, we quickly discovered she suppressed these memories as she didn’t want to remember. She didn’t want to remember how very unhappy a little girl she was. She needed to forget because it hurt her enormously
                knowing that her parents abandoned her. The baby could not understand why she was limited in seeing, feeling, smelling, touching her mom and dad when she needed them so much. She was loved and adored by her grandma. But even a
                newborn knows who their mom is, how she smells, what her voice is as they experienced it for nine months. Baby Monika spent additional three months with her mom and she could not cope with separation that happened just afterwards.
                When she was a baby, she already felt rejected without being able to name it. For four years, Monika was longing for her parents 5 days a week and crying every single weekend as her parents were leaving. Even with limited understanding and
                vocabulary she could explain the heartache to her little self. “I am not good to be with my parents. I am not good enough to see them every day. I am not good, because if I were, they would take me home with
                them…”.
              </p>
              <p>
                Was this belief of a four-year-old girl true and correct?
              </p>
              <p>
                No. it was not. During our session, this gifted midwife admitted her parents had not many options and chose what they believed was best for their daughter (a loving and caring grandma instead of what was at times more “children
                repository” than “a nursery”). But that she concluded over 30 years later.
              </p>
              <p>
                For over 30 years, she was living with a deep, not even consciously realized thought of a small child who believed “I am not good enough, something is wrong with me, I am not lovable the way I am.”. And she was giving all she could
                to be better, to prove she is good enough, to receive love. And as the age of 1,2,3 and 4, at the age of +30 she felt rejected, not important, hurt.
              </p>
              <p>
                Monika was cured in one session. Out of burnout and out of “not-enoughness”.
              </p>

              <h5>
                Wow! Is it really primarily me, not my stressful work environment?
              </h5>

              <p>
                It is both.
              </p>
              <p>
                It is you. Or, to be specific, “a program” your mind runs on – sometimes easy to identify, sometimes not obvious to detect.
              </p>
              <p>
                A program that makes you susceptible to get to burnout.
              </p>
              <p>
                A program that makes you extremely good at pushing yourself, striving to become better, and engaging in what you do.
              </p>
              <p>
                A program that makes you a tireless worker and a dream member of each team.
              </p>
              <p>
                And a program that sparks the flame and burns off your fuel if not refilled with enough recognition, regard, credit, fairness.
              </p>
              <p>
                And it is your work. It might be:
              </p>
              <ul>
                <li> content of your job (way below your abilities, uninteresting, conflicting goals, etc.),</li>
                <li> a job structure (number of positions, lack of clarity in roles and responsibilities, unfairly distributed workload, etc.)</li>
                <li> a company culture (that tolerates discrimination, passive-aggressive jokes or comments, stir unhealthy competition, promotes overly political and nepotism, etc.)</li>
                <li> a management style (micromanagement, lack of time and support for those who do their job well, focus on 1% of mistakes and taking 99% of a great job for granted, overpromising, changing expectations and opinions, etc.)</li>
              </ul>


              <p>
                You know best what exactly makes you feel sick and tired of it, don’t you? A combination of some of these elements can act as a trigger and evoke the same emotions you experienced a long time ago. Feelings that hurt you and that you
                were not able to cope with 20,30,40 years ago. Emotions that switch on “your old program” (”It doesn’t matter how much I try … I am not good enough, I am not important, I don’t matter, I am not lovable, I don’t deserve…, I don’t
                get…”)
              </p>
              <p>
                And because it is both, there are two elements to be addressed.
              </p>
              <p>
                Your job and You.

              </p>
              <h5>
                So, what should you do?
              </h5>
              <h5>
                Your work. Why should you care when you feel betrayed?
              </h5>
              <p>
                Even if the job itself is not the leading cause of your condition, it acts as a trigger. This means it is far from an ideal work environment for most of your co-workers.
              </p>
              <p>
                I believe we all play a role in making our workplaces healthier and better, or more daunting and draining. It’s not just “HR’ who is responsible for it. It’s not only “the leaders” who should realize what’s wrong and correct. We all
                are. Each of us as an employee has a voice and a choice. To do or not to do.
              </p>
              <p>
                As an HR professional, I wholeheartedly encourage you not to wait for the next yearly big anonymous engagement survey that compiles and generalizes answers from hundreds and thousands of employees. Your case matters. Your
                observations and remarks about specific issues are more precious than an overall score of the weighted engagement factors benchmarked against the industry norm (eee… what? Yeah, exactly.).
              </p>
              <p>
                Share your comments with a trusted HR colleague and ask for a team barometer on these particular aspects. Surely you are not the only one bothered by it. It is likely some of your former colleagues left because of some of these. It
                is not just you who feels “I don’t fit in.”.
              </p>
              <p>
                You are like super sensitive canary birds used in coal mines to detect toxic gases before they hurt humans. You can sense the toxin, and you can name the poison. That’s rare.
              </p>
              <p>
                By signalling it, you can help the organization detoxify, address some questionable practices and atrocious behaviours, help lousy leaders step up or move, and stop degenerating morale from spreading. You can help make the
                organization more robust, more transparent, more friendly, fairer, better.
              </p>
              <p>
                Isn’t that what “leadership courage” is in its essence?
              </p>
              <p>
                And isn’t that the essence of “humanity” too? To improve the conditions of our physical, mental and social existence based on our knowledge of the world and ourselves; not only to progress our development on technical, business,
                economic advancement but to enhance the standards of our emotional and moral lives.
              </p>
