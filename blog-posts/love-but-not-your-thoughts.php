            <h5>You may give them your love but not your thoughts.</h5>
              <p>
            I consider this poem one of the most profound, enlightening, and uplifting verses for every human being. It gives hope, relief, and purpose to many of my Rapid Transformational Therapy® clients crushed, hurt, and wounded early in their lives.
</p><p>
            We all went through some tough stuff when we were little. We all had to experience rejection, disappointment, pain. We learnt to appreciate moments of joy, freedom, love, warmth, and carelessness. Even if our childhood was not rosy, from an adult perspective we can understand our parents’ choices and mistakes; we can make sense of circumstances they lived in, we can accept, forgive and move on.
            But some of my clients haven’t experienced joy. They haven’t experienced warmth nor carelessness. They haven’t experienced love. They suffered pain, harm, rejection instead. They suffered terror, hunger and abuse. Often from those who were supposed to provide for them, protect them and adore them – their parents. It is unimaginable for most of us. And it is incomprehensible for a child.
            If I am often left at home with no food since I remember, if my dad beats me with any tool on hand for anything I do, if I am sexually abused… How can I live with that? How can I love myself if my parents never loved me? How can I accept myself if my parents never accepted me? How can I value myself when my parents never valued me? How can I care for myself when my parents never took care of me? How can I find my meaning in life if my life never meant anything to those who brought me to this world?
</p><p>
            “Your children are not your children.<br/>
            They are the sons and daughters of Life’s longing for itself.<br/>
            They come through you but not from you,<br/>
            And though they are with you yet they belong not to you.”<br/>
</p><p>
            Those born to parents who didn’t deserve them don’t want to and don’t have to forgive. But they can, and they do come to peace with themselves, realizing they “came through but not from” their parents. They embrace that Life chose this hurtful path to equip them with the exceptional gift and unique calling. These lines open their hearts, minds, and souls and allow them to look inside and discover who they are and why they are. I love these lines. They are like a magic
            potion to clients who seek serenity.<br/>
            Even if you are not into poetry or suffer school-induced-aversion to try-to-guess “what was on poet’s mind?”, I promise your inner child will instantly and intuitively relate to the words of Kahlil Gibran.

</p><p>  Kahlil Gibran – 1883-1931
</p>
<h5>
            On Children</h5>
<p>
            And a woman who held a babe against her bosom said, Speak to us of Children.<br/>
            And he said:<br/>
            Your children are not your children.<br/>
            They are the sons and daughters of Life’s longing for itself.<br/>
            They come through you but not from you,<br/>
            And though they are with you yet they belong not to you.
</p><p>
            You may give them your love but not your thoughts,<br/>
            For they have their own thoughts.<br/>
            You may house their bodies but not their souls,<br/>
            For their souls dwell in the house of tomorrow, which you cannot visit, not even in your dreams.<br/>
            You may strive to be like them, but seek not to make them like you.<br/>
            For life goes not backward nor tarries with yesterday.
</p><p>
            You are the bows from which your children as living arrows are sent forth.<br/>
            The archer sees the mark upon the path of the infinite, and He bends you with His might that His<br/>
            arrows may go swift and far.<br/>
            Let your bending in the archer’s hand be for gladness;<br/>
            For even as He loves the arrow that flies, so He loves also the bow that is stable.
</p><p>
            From “The Prophet”, 1923.
</p><p>
