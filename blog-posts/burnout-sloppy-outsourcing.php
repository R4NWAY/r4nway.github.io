            <h5> Solution 1: <br />
              “Do you want a sick leave?”</h5>
            <p>
              That’s the first question Annemarie hears after describing her symptoms.
            </p>
            <p>
              “No. I don’t know.” – she hesitates.
            </p>
            <p>
              Annemarie wants to solve her problem, not to be left alone with it.
            </p><em>
              Please note, while the stories of my clients are true, their names and identities have been changed to protect their privacy. Thank you for understanding.
            </em>
<br />
<br />
            <p>
              A doctor closely looks at her as if she was trying to guess what Annemarie’s real intentions are. Does she suffer indeed, or does she look for a safe way out from her company?
            </p>
            <p>
              Then the conversation goes along these lines:
            </p>
            <p>
              – “Have you talked to your boss?” (hmm… they are part of the problem, so not sure I should –responds Annemarie in her mind)
            </p>
            <p>
              – “It’s just work, not your entire life. It is not worth being so stressed about it.” (seriously?! That’s the career I dedicated half of my life so far – she shouts back in her thoughts, but she doesn’t open her mouth)
            </p>
            <p>
              – “I would recommend you switch off your computer at 5 pm and don’t open it till 9 am next day” (And you will check the tests and prepare for my lessons the next day for me? –Annemarie is losing the trust in the physician)
            </p>
            <p>
              – “Have you taken enough holidays? I would recommend you take more rest.” (Have I mentioned I am a teacher? I follow the same days off schedule as all schools in this country.)
            </p>
            <p>
              – “Do you spend enough time outside? Walks in a park or running along the lake help to calm down the stressed mind.” (I don’t but feel like running out just now…– her body is still in the doctor’s room, but her mind is long time gone
              by now)
            </p>
            <p>
              – “Did you tell your “personnel department”?” (yep, they told me to get a doctor’s appointment)
            </p>
            <p>
              Coming to the solution:
            </p>
            <p>
              – “I can give you plant-based medication that will help you sleep. It is not instant. It works for some but not for all, and you need to take it for 2-3 weeks to start feeling whether it works for you or not.” (Sure – mumbles Annemarie
              and finishes to herself: It seems I don’t get anything better here today. I should have taken sick leave when she offered it first… Should I still ask for it?)
            </p>
            <p>
              – “Again, try to relax more… “(thanks, best advice ever)
            </p>
            <p>
              – “If it doesn’t help, I can refer you to a psychiatrist.” (Am I really messed up so much? That’s seriously scary. What was I thinking? I shouldn’t have come here.)
            </p>
            <p>
              I know a lot of great doctors and genuinely respect them as their knowledge saves lives. When they suspect burnout, they offer one of the two:
            </p>
            <ol>
              <li> Sick leave with medication for physical symptoms (headaches, nausea, sleeping pills, etc.)</li>
              <li> Or referral to a psychiatrist or psychotherapist (if they feel the case is more severe).</li>
            </ol>
            <p>
              For many physicians, burnout is simply not their expertise. We, the HR community, know it. But we sometimes want to believe we did all we should be sending them to doctors. When an employee sends us a sick-leave, we want to think they
              are getting somehow better.
            </p>
            <p>
              Sometimes they do. Too often, that’s not the case.
            </p>


            <h5> Before you cast the first stone</h5>
            <p>
              If you are not an HR professional, please contemplate for a moment what would you do:
            </p>
            <ul>
              <li> If you see a person struggling at work and you suspect they also have other problems? </li>
              <li> If you see that their boss is far from ideal, but so are many others? </li>
              <li> If you know, they are overloaded and not recognized enough, but so are others and coping way better? </li>
              <li> Perhaps they just are overly sensitive by nature? Or maybe they are going through difficult times at home, and it makes them more vulnerable? </li>
            </ul>
            <p> How much would you and should you intrude upon somebody’s privacy, even with the clearest
              intentions to help, given a very professional set up of your relationship with this person?</p>

            <p> Not as straightforward as it seems, is it?</p>



            <h5> Definition</h5>

            <p>

              Burnout is defined as a prolonged response to chronic emotional and interpersonal stressors on the job.
              It is defined by the three dimensions of exhaustion, cynicism, and professional inefficacy. (<a href="https://pubmed.ncbi.nlm.nih.gov/11148311/">Christina Maslach & Michael Leiter</a>)
            </p>
            <p>
              Burnout places the individual stress experience within a larger organizational context of people’s relation to their work content, conditions, and relations. What comes with it is the widely accepted belief that the work environment is
              the main reason and the sole root cause of the problem.

            </p>

            <h5> In HR, we know it is not accurate. </h5>
            <p>
              If the work environment solely caused burnout, most people experiencing the same working conditions would be affected. Yet, most don’t. We sense, there must be some individual factors making some people predisposed more than others.
            </p>
            <p>
              I don’t excuse nor justify hostile work environments, physical, mental, and emotional overload, unhealthy working relations, lack of support, ruthless bosses, nepotism etc. But rather than seeing work environments as a sole root cause,
              I deem they act as triggers to spark and activate the response in some individuals with let’s call it “specific program” or
              coping mechanisms (explained in detail in <a href="burnout-work-trigger.php">Burnout – your work is just the trigger, not a root cause.</a>)
            </p>
            <p>
              HR teams often just don’t know how to go about it.
            </p>
            <h5> Burnout aboard. What does HR do?</h5>
            <p>
              I look myself in the mirror here and share what I have experienced during my 20 years of progressive experience in local, regional, and global HR leadership roles across a few industries, as well as practices I heard from my fellow HR
              executives and my clients on the therapist’s couch.
            </p>
            <p>
              It is not an exhaustive list, but the most frequent routes (in random order):
            </p>
            <ol>
              <li> recommendation to see the doctor; </li>
              <li> attempt to help via coaching (by HR, by a line manager, by external coach);</li>
              <li> referral to the corporate employee assistance program (external helpline where they can call and confidentially speak about their problems);</li>
              <li> offering days off (so they can rest and pull themselves together again).</li>
            </ol>
            <p>
              That is what HR teams in big organizations usually have at their disposal. In small companies, often even that is not available. Burnout seems to be quite just though affecting individuals in all sorts of organizations: big and small,
              public and private, money-making and non-profit. It is there and we, HR…
            </p>
            <h5> ...we outsource the problem.</h5>
            <p>
              And that is ok. Obtaining services from an outside supplier with expertise in the domain is a standard business practice. I believe it is also the right approach in this case. For a few reasons:
            </p>

            <ol>
              <li> Nobody expects HR to be burnout experts or therapists. </li>

              <li> Even if we all were, the nature of a relationship with an HR professional vs. a therapist is very different. We shouldn’t shift the balance.</li>

              <li> As harsh and as corporate as it may sound, it also seems like a perfectly reasonable approach to focus most of time and energy on the vast majority of the working population, not the few ones who currently actively disengage and
                are likely on the way out (that’s reality).</li>
            </ol>
            <h5> So, are we good here?</h5>
            <p>
              Not quite yet.
              Outsourcing is ok. But sloppy outsourcing is… well, sloppy.
            </p>
            <p> Let’s have a closer look.</p>

            <h5> Solution 2: <br />
              Let’s coach it out.</h5>
            <p>
              Dave was lucky. That’s what he thought. His leaders valued him as a great contributor and saw potential in him. He was doing a fantastic job. They didn’t want to lose him. Together with HR, they offered him the support of an
              experienced external coach to deal with his issue. Dave was pleased to hear that company wants to support him and invest in his development. Full of hopes started his coaching process.
            </p>
            <p>
              During the first session, he discussed all symptoms with his coach, analyzed when it started and went through all circumstances in his work environment that make him suffer. He visualized what will happen if nothing changes in 6 months
              (negative emotional image), and what if he manages to revert the situation (positive emotional image). Dave decided he wants the good one and not the bad one to be his future and made a list of what can he do to address some of the
              situations and change his response to those that cannot be changed. Dave was optimistic. He hasn’t felt that hopeful in months.
            </p>
            <p>
              He genuinely wanted to follow through. It didn’t work. When his line manager asked him as usual at 4 pm for the new version of the report that “needs to be done urgently by the end of the day as another executive in different time zone
              must have it first thing in the morning”, he fell apart. He couldn’t pull any of these assertive words he practiced two days before with his coach. He was not able to push back. He felt like crying, screaming, smashing his laptop on
              his boss’s head. He threw his laptop on his desk with resignation thinking whether he should stay or take work home. Without a doubt, additional 5 hours in front of the screen was his plan for tonight.
            </p>
            <p>
              Much later that night, Dave was exhausted, yet couldn’t sleep again. “What is wrong with me? Why can’t I just repeat one simple sentence? Why am I such a pushover?” He could not understand what was holding him back. “And how I could be
              so stupid to think the company values me? They just want me to do more and more, nothing will change. My a$$hole boss will not change. They don’t care!”.
            </p>
            <p>
              Dave was devastated.

            </p>

            <h5> Solution 3: <br />
              Employee Assistance Program</h5>
            <p>
              Great stuff. If you know what you buy, not just what you hope you contracted.
            </p>
            <p>
              Monika, midwife in her thirties who experienced burnout, decided to call the Helpline number. She answered many questions about her feelings, symptoms, reasons why she felt this way, what was happening in her job, the work environment,
              etc. The woman on the other side of the phone seemed very pleasant and professional. After good 20 minutes of talk, she confirmed she suspected Monika was experiencing burnout and run a checklist of “self-help” questions: “Have you
              tried to meditate to sleep better? No? Perhaps you should try. Do you meet with friends regularly? Too busy to do that? Try to do that twice a week. Do you exercise regularly? Perhaps running could help? Etc…”. She recommended Monika
              to have an open discussion with her boss and see a general doctor for further treatment.
            </p>
            <p>
              At the end of the call, Monika felt better. She found an ear to offload what bothered her and didn’t feel rushed or judged.
            </p>
            <p>
              Within an hour, she got upset and realized she hadn’t received any help at all. She was back to square one. Exhausted, bitter and lonely.
            </p>


            <h5> Solution 4:<br />
               A short break from work</h5>
            <p>
              Dave took a week off. To rest, to think, to relax. His HR Business Partner genuinely hoped it would help him recover. Additionally, he would go down with his ridiculous time off balance as he had many accrued holidays from previous
              years. A small win, at least.
            </p>
            <p>
              Dave didn’t go on holiday. His wife could not take days off with short notice. He stayed home and searched all he could find about burnout. He kept asking himself, “Why the heck it happens to me? Why do I feel so sick and tired? It
              didn’t seem to be this way when I started”. He didn’t find answers.
            </p>
            <p>
              His sense of responsibility made him very itchy when he saw emails popping on his mobile screen. He tried to restrain himself from answering, yet when he saw a report send by his subordinate directly to his boss, he couldn’t hold back
              and opened it. The quality was below what Dave could accept, so he jumped in to respond. How could he relax and switch off when things didn’t go right without him? If he switched off completely, he knew the week after would mean lots
              of overtime to do the damage control.
            </p>
            <p>
              This week off didn’t solve Dave’s problem.
            </p>
            <p>
              (If you want to know what helped Dave and discover Dave, Annemarie, and Monika’s burnout and recovery stories, click <a href="burnout-work-trigger.php">here</a>. Some of the insights and suggestions can help you or somebody around who experiences burnout.)
            </p>


            <h5> HR confession</h5>
            <p>
              During my entire HR career, never have I ever called, checked, or discussed with my colleagues what kind of help employees receive when they reach the Employee Assistance Program. Nor I heard any of my HR colleagues doing something
              like that. Of course, we saw reports and invoices for how many people called with which problems, but not even once we discussed support our people received. It was never on the agenda; it was never a priority; it was never even
              remotely interesting. Shame on me!
            </p>
            <p>
              I hope to hear back that I grew up in HR wild west, and the average is vastly different.
            </p>
            <p>
              I have never had a name of a doctor or a therapist specializing in burnout to share with an employee who struggled. Neither had any (of dozens) of my HR colleagues. I considered myself quite compassionate. I always had time, an ear,
              and a heart for people who struggled. We talk, we listen, we support emotionally, we offer days off and medical help. And then we send them out. To nowhere. Or to figure-it-out-yourself. OUCH!
            </p>
            <p>
              I genuinely think the employee assistance program, seeing the doctor, taking a few days off, or having somebody to talk to are good ideas. They all can help under one condition: you must be lucky to step into the right professional’s
              office.
            </p>
            <p>


              <h5> Is leaving things to luck (a.k.a.: sloppy outsourcing) the best we can do?</h5>
              <p>
                No need to answer, we all know.
              </p>
              <p>
                If that were your child going through this experience, you would fight for them with the madness and desperation of an antelope mother whose baby is surrounded by a pack of hyenas.
              </p>
              <p>
                If that were your spouse who suffers, you would fight by their side, dropping everything to find the best doctor, therapist, or magician in your town or on the planet in no time at all.
              </p>
              <p>
                We would not outsource them out to “figure-it-out-yourself”.
              </p>
              <p>
                Burnout employees are desperate for help and will try many things, anything. But each next solution that turns out ineffective confirms that help is not available to them and that nobody cares about them, nor is genuinely interested
                in their problem and them as a person.
              </p>
              <h5> An easy fix</h5>
              <p>
                You don’t need to show up with the madness and despair of antelope’s mom. Just turn up the volume on the “human” knob and come with “resources”: a shortlist of verified professionals who deal with burnout.
              </p>
              <p>
                Save coaching for other occasions. I am a big fan of coaching, but I have not met anybody coached out of burnout. Coaching is a process aiming to improve performance and focusing on the ‘here and now’ rather than on the distant past
                or future. And in the case of burnout, it is likely “a distant future” might need attention.
              </p>
              <p>
                Follow up later whether the solution you offered worked for them.
              </p>

              <h5> A tiny difference that can make a world of difference.</h5>



              <p> Want to stand out?</p>
              <p>
                “People are our greatest assets.”… proudly stated on your company website? People / talent / dignity & respect / creating an outstanding environment for exceptional people / taking care of each other / or some version of it, are some
                of your core values too?
              </p>
              <p>
                Consider reimbursing the burnout treatment cost if not covered by standard health insurance. Show that you care about the wellbeing of your people. Not just your “greatest asset”, but engaged, damn good, hard-working people. They did
                not burn out just after the probation period; they are tried and tested.
              </p>
              <p>
                It is way cheaper than long term absence and the cost of backfilling the “regretted loss”, no question. That is a good rational argument for your CFO.
              </p>
              <p>
                The irrational one is that we all want to look ourselves in the mirror and smile when we get home.

              </p>
              <h5> What do you do?</h5>
              <p>
                Please share how your organization is addressing burnout. I welcome the perspectives of my fellow HR colleagues and employees who experienced or witnessed burnout and leaders who had to deal with this problem in their teams.
              </p>
